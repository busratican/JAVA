
public class Increment {
            public static void main(String[] args){
            	
            	int c=5;
            	
            	
            	System.out.println(c);
            	System.out.println(c++);
            	System.out.println(c);
            	System.out.println();
            	
            	System.out.println(c);
            	System.out.println(++c);
            	System.out.println(c);
            	System.out.println();
            	
            	System.out.println(c);
            	System.out.println(c--);
            	System.out.println(c);
            	System.out.println();
            	
            	System.out.println(c);
            	System.out.println(c);
            	System.out.println(--c);
            
            }
}
